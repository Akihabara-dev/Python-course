# English: 
# It is a function that gives us a series of values.
# It will allow us to iterate.
# Example 1 >>> range(number)
# Example 2 >>> range(start, number)
# Example 3 >>> range(start, number, step)

# Spanish: 
# Es una función que nos entrega una serie de valores.
# Nos va a permitir iterar.
# Ejemplo 1 >>> range(número)
# Ejemplo 2 >>> range(inicio, número)
# Ejemplo 3 >>> range(inicio, número, paso)

for i in range(3):
    print (i)
print ("Done")


for i in range(10, 15):
    print (i)
print ("Done")

for i in range(100, 20, -20):
    print (i)
print ("Done")